func generatedResourcePaths(version *declarestv1alpha1.CRDGeneratorVersion, obj *unstructured.Unstructured) (string, string, error) {
	collectionPath := ""
	if version != nil {
		collectionPath = version.CollectionPath
	}
	if override, _, _ := unstructured.NestedString(obj.Object, "spec", "collectionPathOverride"); strings.TrimSpace(override) != "" {
		collectionPath = override
	}
	collectionPath, err := normalizeGeneratedResourcePath(collectionPath)
	if err != nil {
		return "", "", fmt.Errorf("collection path is invalid: %w", err)
	}
	name := strings.TrimSpace(obj.GetName())
	if name == "" {
		return "", "", fmt.Errorf("metadata.name is required")
	}
	return collectionPath, logicalPathForGeneratedCR(collectionPath, obj), nil
}

func normalizeGeneratedResourcePath(raw string) (string, error) {
	value := strings.TrimSpace(raw)
	if value == "" {
		return "/", nil
	}
	if !strings.HasPrefix(value, "/") {
		value = "/" + value
	}
	return resource.NormalizeLogicalPath(value)
}

func generatedResourceAnnotation(obj *unstructured.Unstructured, key string) string {
	if obj == nil {
		return ""
	}
	return strings.TrimSpace(obj.GetAnnotations()[key])
}

func logicalPathForGeneratedCR(collectionPath string, obj *unstructured.Unstructured) string {
	name := strings.TrimSpace(obj.GetName())
	if name == "" {
		return ""
	}
	trimmedCollection := strings.Trim(strings.TrimSpace(collectionPath), "/")
	if trimmedCollection == "" {
		return "/" + name
	}
	return "/" + trimmedCollection + "/" + name
}
